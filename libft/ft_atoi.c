int		ft_atoi(const char *str)
{
	int	i;
	int sign;
	int output;

	i = 0;
	sign = 1;
	output = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
		output = (output * 10) + str[i++] - '0';
	return (sign * output);
}
