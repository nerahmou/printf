#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		length;
	int		i;
	char	*dup;

	length = ft_strlen(src);
	dup = ft_strnew(length);
	if (!dup)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
