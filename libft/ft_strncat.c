#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	int		i;
	size_t	j;

	i = 0;
	j = 0;
	while (dest[i])
		i++;
	while (*src && j < n)
	{
		dest[i++] = *src;
		src++;
		j++;
	}
	dest[i] = 0;
	return (dest);
}
